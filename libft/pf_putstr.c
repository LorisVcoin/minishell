#include "ft_printf.h"

int	pf_putstr(char *s)
{
	int	i;

	i = 0;
	if (s == NULL)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (s[i])
		pf_putchar(s[i++]);
	return (i);
}
